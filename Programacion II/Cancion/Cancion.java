public class Cancion {
    String Titulo;
    String artist;
    String Album;
    int Duracionsec;
    String Type;
    Cancion (String Titulo,String artist,String Album,int Duracionsec,String Type){
        this.Titulo = Titulo;
        this.artist = artist;
        this.Album = Album;
        this.Duracionsec = Duracionsec;
        this.Type = Type;
    }
}
